class TestTypeNormalizationConsistency:
    """Test that type normalization is consistent across dialects."""
    
    def test_varchar_preserved(self):
        """VARCHAR should be preserved across all dialects."""
        oracle = OracleAdapter()
        postgres = PostgresAdapter()
        mysql = MySQLAdapter()
        sqlite = SQLiteAdapter()
        
        assert oracle.normalize_type('VARCHAR(255)') == 'VARCHAR(255)'
        assert postgres.normalize_type('VARCHAR(255)') == 'VARCHAR(255)'
        assert mysql.normalize_type('VARCHAR(255)') == 'VARCHAR(255)'
        assert sqlite.normalize_type('VARCHAR(255)') == 'VARCHAR(255)'
    
    def test_integer_preserved(self):
        """INTEGER should be preserved across all dialects."""
        oracle = OracleAdapter()
        postgres = PostgresAdapter()
        mysql = MySQLAdapter()
        sqlite = SQLiteAdapter()
        
        assert oracle.normalize_type('INTEGER') == 'INTEGER'
        assert postgres.normalize_type('INTEGER') == 'INTEGER'
        assert mysql.normalize_type('INTEGER') == 'INTEGER'
        assert sqlite.normalize_type('INTEGER') == 'INTEGER'
    
    def test_numeric_canonical(self):
        """NUMERIC should be the canonical form for numeric types."""
        oracle = OracleAdapter()
        postgres = PostgresAdapter()
        
        assert oracle.normalize_type('NUMBER') == 'NUMERIC'
        # Postgres NUMERIC is already canonical
        assert postgres.normalize_type('NUMERIC') == 'NUMERIC'


class TestCaseInsensitivity:
    """Test that type normalization handles case variations."""
    
    def test_oracle_lowercase(self):
        adapter = OracleAdapter()
        assert adapter.normalize_type('varchar2(255)') == 'VARCHAR(255)'
        assert adapter.normalize_type('number') == 'NUMERIC'
    
    def test_postgres_lowercase(self):
        adapter = PostgresAdapter()
        assert adapter.normalize_type('serial') == 'INTEGER'
        assert adapter.normalize_type('text') == 'VARCHAR'
    
    def test_mysql_lowercase(self):
        adapter = MySQLAdapter()
        assert adapter.normalize_type('tinyint') == 'SMALLINT'
        assert adapter.normalize_type('datetime') == 'TIMESTAMP'
    
    def test_sqlite_lowercase(self):
        adapter = SQLiteAdapter()
        assert adapter.normalize_type('text') == 'VARCHAR'
        assert adapter.normalize_type('real') == 'FLOAT'


class TestEdgeCases:
    """Test edge cases and unusual type definitions."""
    
    def test_type_with_spaces_in_precision(self):
        """Test types with spaces in precision/scale."""
        oracle = OracleAdapter()
        # NUMBER ( 10 , 2 ) with spaces
        assert oracle.normalize_type('NUMBER(10, 2)') == 'NUMERIC(10, 2)'
        assert oracle.normalize_type('NUMBER( 10 , 2 )') == 'NUMERIC( 10 , 2 )'
    
    def test_unknown_type_preserved(self):
        """Unknown types should be preserved as-is."""
        oracle = OracleAdapter()
        assert oracle.normalize_type('CUSTOM_TYPE') == 'CUSTOM_TYPE'
    
    def test_empty_string(self):
        """Empty string should be returned as-is."""
        oracle = OracleAdapter()
        assert oracle.normalize_type('') == ''
    
    def test_type_without_base(self):
        """Type that doesn't match pattern should be returned as-is."""
        oracle = OracleAdapter()
        assert oracle.normalize_type('123') == '123'


