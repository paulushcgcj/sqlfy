"""
Tests for dialect adapters and multi-dialect support.

Tests type normalization and auto-increment detection across:
- Oracle (default)
- PostgreSQL  
- MySQL
- SQLite
"""

import pytest
from sqlfy.dialects import (
    DialectAdapter,
    OracleAdapter,
    PostgresAdapter,
    MySQLAdapter,
    SQLiteAdapter,
)


class TestDialectFactory:
    """Test DialectAdapter.for_dialect() factory method."""
    
    def test_oracle_dialect(self):
        adapter = DialectAdapter.for_dialect('oracle')
        assert isinstance(adapter, OracleAdapter)
        assert adapter.name == 'oracle'
    
    def test_postgres_dialect(self):
        adapter = DialectAdapter.for_dialect('postgres')
        assert isinstance(adapter, PostgresAdapter)
        assert adapter.name == 'postgres'
    
    def test_postgresql_alias(self):
        adapter = DialectAdapter.for_dialect('postgresql')
        assert isinstance(adapter, PostgresAdapter)
    
    def test_mysql_dialect(self):
        adapter = DialectAdapter.for_dialect('mysql')
        assert isinstance(adapter, MySQLAdapter)
        assert adapter.name == 'mysql'
    
    def test_sqlite_dialect(self):
        adapter = DialectAdapter.for_dialect('sqlite')
        assert isinstance(adapter, SQLiteAdapter)
        assert adapter.name == 'sqlite'
    
    def test_unsupported_dialect(self):
        with pytest.raises(ValueError, match='Unsupported dialect'):
            DialectAdapter.for_dialect('mssql')
    
    def test_case_insensitive(self):
        adapter = DialectAdapter.for_dialect('POSTGRES')
        assert isinstance(adapter, PostgresAdapter)


class TestOracleAdapter:
    """Test Oracle dialect adapter."""
    
    def test_normalize_varchar2(self):
        adapter = OracleAdapter()
        assert adapter.normalize_type('VARCHAR2(255)') == 'VARCHAR(255)'
    
    def test_normalize_number(self):
        adapter = OracleAdapter()
        assert adapter.normalize_type('NUMBER') == 'NUMERIC'
        assert adapter.normalize_type('NUMBER(10)') == 'NUMERIC(10)'
        assert adapter.normalize_type('NUMBER(10,2)') == 'NUMERIC(10,2)'
    
    def test_normalize_nvarchar2(self):
        adapter = OracleAdapter()
        assert adapter.normalize_type('NVARCHAR2(100)') == 'NVARCHAR(100)'
    
    def test_normalize_long(self):
        adapter = OracleAdapter()
        assert adapter.normalize_type('LONG') == 'TEXT'
    
    def test_normalize_raw(self):
        adapter = OracleAdapter()
        assert adapter.normalize_type('RAW(16)') == 'BINARY(16)'
    
    def test_normalize_unchanged(self):
        adapter = OracleAdapter()
        assert adapter.normalize_type('INTEGER') == 'INTEGER'
        assert adapter.normalize_type('VARCHAR(255)') == 'VARCHAR(255)'
    
    def test_is_auto_increment(self):
        adapter = OracleAdapter()
        assert adapter.is_auto_increment('id NUMBER GENERATED BY DEFAULT AS IDENTITY')
        assert adapter.is_auto_increment('id NUMBER GENERATED ALWAYS AS IDENTITY')
        assert not adapter.is_auto_increment('id NUMBER')
        assert not adapter.is_auto_increment('id NUMBER PRIMARY KEY')


